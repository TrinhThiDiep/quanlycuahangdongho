    @Override
    public void refresh(boolean reset) {
        try {

            List<Vendor> items = VendorDAO.instance.findAll(txtSearchQuery.getText());

            DefaultTableModel model = (DefaultTableModel) tblDataGrid.getModel();
            model.setRowCount(0);

            for (Vendor n : items) {
                Object dataRow[] = new Object[2];
                dataRow[0] = n.getId();
                dataRow[1] = n.getTitle();
                model.addRow(dataRow);
            }
        } catch (SQLException ex) {
            Logger.getLogger(DlgCreate.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

