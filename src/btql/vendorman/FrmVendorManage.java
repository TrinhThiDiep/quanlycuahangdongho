    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int selected = getCurrentSelected();
        if (selected == -1) {
            return;
        }

        int result = JOptionPane.showConfirmDialog(this, "Bạn có muốn xoá ?");

        if (result == JOptionPane.YES_OPTION) {
            try {
                VendorDAO.instance.remove(selected);
                refresh(true);
            } catch (SQLException ex) {
                Logger.getLogger(FrmVendorManage.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    int getCurrentSelected() {
        // row selected
        int selectId = tblDataGrid.getSelectedRow();
        if (selectId == -1) {
            return -1;
        }

        int id = Integer.valueOf(tblDataGrid.getModel().getValueAt(selectId, 0).toString());

        return id;
    }

