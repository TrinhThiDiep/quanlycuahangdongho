    private List<String> getFormErrors() throws SQLException {
        List<String> errors = new ArrayList();
        if (txtTitle.getText().equals("")) {
            errors.add("Tiêu đề không được bỏ trống");
        }

        if (cbVendor.getModel().getSelectedItem() == null) {
            errors.add("Nhà cung cấp không được bỏ trống");
        }

        if (cbCategory.getModel().getSelectedItem() == null) {
            errors.add("Danh mục không được bỏ trống");
        }

        return errors;
    }

