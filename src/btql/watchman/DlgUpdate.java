    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        try {

            List<String> errors = getFormErrors();
            if (!errors.isEmpty()) {
                JOptionPane.showMessageDialog(this, String.join("\n", errors), "Lỗi", JOptionPane.ERROR_MESSAGE);
                return;
            }

            Watch watch = new Watch();
            watch.setId(defaultModel.getId());
            watch.setTitle(txtTitle.getText());
            watch.setDescription(txtDescription.getText());
            watch.setQuantity((int) spQuantity.getValue());
            watch.setUnitPrice((int) spUnitPrice.getValue());

            watch.setVendorId(((Vendor) cbVendor.getModel().getSelectedItem()).getId());
            watch.setCategoryId(((Category) cbCategory.getModel().getSelectedItem()).getId());
            watch.setUserId(Config.getCurrentUserId());

            WatchDAO.instance.update(watch);
            ((DataGridForm) getOwner()).refresh(true);

            // create
            // update DB
            dispose();
        } catch (SQLException ex) {
            Logger.getLogger(DlgCreate.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

