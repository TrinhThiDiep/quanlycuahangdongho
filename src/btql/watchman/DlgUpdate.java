    private Watch defaultModel;

    void setModel(int id) throws Exception {
        Watch watch = WatchDAO.instance.findById(id);
        txtId.setText(Integer.toString(watch.getId()));
        txtTitle.setText(watch.getTitle());
        txtDescription.setText(watch.getDescription());
        spQuantity.setValue(watch.getQuantity());
        spUnitPrice.setValue(watch.getUnitPrice());

        for (int i = 0; i < cbVendor.getModel().getSize(); ++i) {
            if (cbVendor.getModel().getElementAt(i).getId() == watch.getVendorId()) {
                cbVendor.getModel().setSelectedItem(cbVendor.getModel().getElementAt(i));
            }
        }

        for (int i = 0; i < cbCategory.getModel().getSize(); ++i) {
            if (cbCategory.getModel().getElementAt(i).getId() == watch.getCategoryId()) {
                cbCategory.getModel().setSelectedItem(cbCategory.getModel().getElementAt(i));
            }
        }

        defaultModel = watch;
    }

